# concatenate two strings
print("Hello" + "World")
a = "Hello"
b = "world!"

print(a+b)  # Hello world!

# Repetition
print('halo'*5)  # halohalohalohalohalo
print('wk'*5)  # wkwkwkwkwk

# Slicing
nama = 'Nanaz'
print(len(nama))  # ana

# Upper
name = 'Nana Casmana Ade Wikarta'
print(name.upper()) # NANA CASMANA ADE WIKARTA

# Strip
myname = 'Nana Casmana Ade Wikarta'
print(myname.strip)  # Nana Casmana Ade Wikarta

# Replace
ketawa = 'HAHAHAHHA'
print(ketawa.replace('HA', 'he'))

# Split
anam = 'Nana Casmana Ade Wikarta'
print(anam.split(' '))
print(anam.split('a'))
